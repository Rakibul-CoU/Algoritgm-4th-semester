using namespace std;
int main()
{
    int r, c, i, j;
    cin >> r >> c;
    int a[r+2][c+2], trans[r+2][c+2];
    for(i = 0; i < r; ++i)
    for(j = 0; j < c; ++j)
    {
        cin >> a[i][j];
    }
    for(i = 0; i < r; ++i)
        for(j = 0; j < c; ++j)
        {
            trans[j][i]=a[i][j];
        }
    cout << endl << "Transpose of Matrix: " << endl;
    for(i = 0; i < c; ++i)
        for(j = 0; j < r; ++j)
        {
            cout << " " << trans[i][j];
            if(j == r - 1)
                cout << endl << endl;
        }

    return 0;
}
